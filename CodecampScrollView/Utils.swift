import Foundation
import UIKit

extension UIColor {
    class func gayColor() -> UIColor {
        return UIColor(red: CGFloat(0xFF)/255, green: CGFloat(0x14)/255, blue: CGFloat(0x93)/255, alpha: 1.0)
    }
}